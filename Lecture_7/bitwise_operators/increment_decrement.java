package bitwise_Operators;

public class increment_decrement {
    public static void main(String[] args){
        int a = 10;
        System.out.println(++a);
        a = 10;
        System.out.println(a++);
        a++;
        ++a;
        System.out.println(a);
        int ans = 2*3/2;  // * occurs first , follow table of precedence!
        System.out.println(ans);
    }
}
